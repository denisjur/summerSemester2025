	// |                    | /               | 2              | Links          | x = 4 / 2;                 |
	// |                    | %               | 2              | Links          | x = 4 % 3;                 |
	// -------------------------------------------------------------------------------------------------------
	// | Verbund            | +=, *=, ...     | 13             | Rechts         | x += 1;                    |
	// -------------------------------------------------------------------------------------------------------
	// | Inkrement          | ++              | 1              | Rechts         | x++;   oder   ++x;         |
	// | Dekrement          | --              | 1              | Rechts         | x--;   oder   --x;         |
	// -------------------------------------------------------------------------------------------------------
	// | Vorzeichen         | -               | 1              | Rechts         | x = -x;                    |
	// -------------------------------------------------------------------------------------------------------
	// | Vergleich          | ==              | 6              | Links          | x == 42                    |
	// |                    | !=              | 6              | Links          | x != 4                     |
	// |                    | <               | 5              | Links          | x < 4                      |
	// |                    | <=              | 5              | Links          | x <= 5                     |
	// |                    | >               | 5              | Links          | x > 6                      |
	// |                    | >=              | 5              | Links          | x >= 7                     |
	// -------------------------------------------------------------------------------------------------------
	// | Logisches Nicht    | !               | 1              | Rechts         | !x                         |
	// | Logisches UND      | &&              | 10             | Links          | x && y                     |
	// | Logisches ODER     | ||              | 11             | Links          | x || y                     |
	// -------------------------------------------------------------------------------------------------------
	// | Linksschieben      | <<              | 4              | Links          | x << 1                     |
	// | Rechtsschieben     | >>              | 4              | Links          | x >> 1   (mit Vorzeichen)  |
	// |                    | >>>             | 4              | Links          | x >>> 1  (ohne Vorzeichen) |
	// -------------------------------------------------------------------------------------------------------
	// | Bitweises UND      | &               | 7              | Links          | x & 1                      |
	// | Bitweises ODER     | |               | 9              | Links          | x | 1                      |
	// | Exklusives ODER    | ^               | 8              | Links          | x ^ 1                      |
	// =======================================================================================================

